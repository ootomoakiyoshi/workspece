public class If01 {
  public static void main(String[] args) {
      int number = 7;
      if (number <3) {
        System.out.println(number + "は3未満です");
      }
      if (number < 5) {
          System.out.println(number + " は5未満です");
      }
      if (number < 10) {
          System.out.println(number + " は10未満です");
      }
      if (number < 15) {
          System.out.println(number + " は15未満です");
      }
      if (number <20) {
        System.out.println(number + "は20未満です");
      }
  }
}