public class Switch {
  public static void main(String[] args){
    int n = 5;
    switch (n) {
      case 1:
       System.out.println("大吉");
       break;
      case 2:
       System.out.println("吉");
       break;
      case 3:
       System.out.println("中吉");
       break;
       default:
        System.out.println("凶");
        break;
    }
  }
}
