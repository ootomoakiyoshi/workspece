public class App {
    public static void main(String[] args) throws Exception {
        // 数値の17を出力
        System.out.println(17);
        // 5+3を足した値の出力
        System.out.println(5 + 3);
         // 5+3の文字列を出力。
         System.out.println("5 + 3");
        //  掛け算・割り算
        System.out.println(5 * 3);
        System.out.println(2 % 4);
        System.out.println("3 + 7");
    }
}
